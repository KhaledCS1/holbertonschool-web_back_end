#!/usr/bin/env python3
"""
This module provides basic mathematical operations such as
addition using type annotations.
"""


def concat(str1: str, str2: str) -> str:
    """
    Adds two string-point strings and returns the result.

    Parameters:
    str1 (str): The first number.
    str2 (str): The second number.

    Returns:
    str: The concat of str1 and str2.
    """
    return str1 + str2
